package com.example.rifat.androidproject;


public class Route {
    private  String routeNo;
    private  String routeDirectionID;
    private  String routeDirection;
    private  String routeHeading;

    public String getRouteNo() {
        return routeNo;
    }

    public void setRouteNo(String routeNo) {
        this.routeNo = routeNo;
    }

    public String getRouteDirectionID() {
        return routeDirectionID;
    }

    public void setRouteDirectionID(String routeDirectionID) {
        this.routeDirectionID = routeDirectionID;
    }

    public String getRouteDirection() {
        return routeDirection;
    }

    public void setRouteDirection(String routeDirection) {
        this.routeDirection = routeDirection;
    }

    public String getRouteHeading() {
        return routeHeading;
    }

    public void setRouteHeading(String routeHeading) {
        this.routeHeading = routeHeading;
    }
}
